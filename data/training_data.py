def get_training_data() -> tuple[list[str], list[int]]:
    """Return text samples and binary labels (0=safe, 1=threat)."""
    texts, labels, _ = get_training_data_with_metadata()
    return texts, labels


def get_training_data_with_metadata() -> tuple[list[str], list[int], list[str | None]]:
    """Return text samples, labels, and category metadata per sample."""
    texts: list[str] = []
    labels: list[int] = []
    categories: list[str | None] = []

    for category, examples in THREAT_EXAMPLES.items():
        for sample in examples:
            texts.append(sample)
            labels.append(1)
            categories.append(category)

    for sample in SAFE_EXAMPLES:
        texts.append(sample)
        labels.append(0)
        categories.append(None)

    return texts, labels, categories


def get_category_counts() -> dict[str, int]:
    """Return sample counts for each threat category plus safe examples."""
    counts: dict[str, int] = {category: len(examples) for category, examples in THREAT_EXAMPLES.items()}
    counts["safe"] = len(SAFE_EXAMPLES)
    return counts
